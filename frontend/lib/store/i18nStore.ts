/* Store */
interface I18nState {
  language: Language;
  setLanguage: (lang: Language) => void;
}

function readInitialLanguage(): Language {
  const persisted = readPersistedState<Language>(I18N_LANGUAGE_STORAGE_KEY);
  return persisted === "uk" ? "uk" : "en";
}

export const useI18nStore = create<I18nState>((set) => ({
  language: "en",
  setLanguage: (lang) => {
    writePersistedState(I18N_LANGUAGE_STORAGE_KEY, lang);
    set({ language: lang });
  },
}));

let i18nHydrated = false;

export function hydrateI18nStoreFromPersistence(): void {
  if (i18nHydrated) return;
  i18nHydrated = true;
  useI18nStore.setState({ language: readInitialLanguage() });
}

/* Hook */
export function useTranslation() {
  const language = useI18nStore((s) => s.language);

  const t = useCallback((key: string): string => {
    return translations[language]?.[key] ?? translations.en[key] ?? key;
  }, [language]);

  return { t, language };
}

/* Non-hook accessor for use outside React components */
export function getTranslation(key: string): string {
  const lang = useI18nStore.getState().language;
  return translations[lang]?.[key] ?? translations.en[key] ?? key;
}
